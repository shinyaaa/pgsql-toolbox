def _pg_ctl_action(project_dir: Path, data_dir: Path, port: int,
                   action: str, label: str):
    """Run pg_ctl action (start/stop/restart/reload) on one instance."""
    pg_ctl = project_dir / "bin" / "pg_ctl"
    if not pg_ctl.exists():
        logger.warning(f"pg_ctl not found for {label}, skipping")
        return
    if not data_dir.exists():
        logger.warning(f"Data dir {data_dir} not found for {label}, skipping")
        return

    cmd = [str(pg_ctl), "-D", str(data_dir)]
    if action == "start":
        cmd.extend(["-o", f"-p {port}", "start"])
    elif action == "stop":
        cmd.extend(["stop", "-m", "fast"])
    elif action == "restart":
        cmd.extend(["-o", f"-p {port}", "restart"])
    elif action == "reload":
        cmd.append("reload")
    else:
        raise ValueError(f"Unknown action: {action}")

    logger.info(f"{action.capitalize()} {label} (port {port})...")
    result = subprocess.run(
        cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, text=True,
        timeout=30,
    )
    if result.returncode != 0 and action != "stop":
        raise RuntimeError(f"pg_ctl {action} failed for {label} (exit {result.returncode})")


def stop_cluster(project_dir: Path, name: str, port: int,
                 standbys: list):
    """Stop all instances in a cluster (standbys first, then primary)."""
    # Stop standbys in reverse order
    for sb in reversed(standbys):
        idx = sb["standby_index"]
        sb_data = standby_data_dir(project_dir, idx)
        sb_p = standby_port(port, idx)
        _pg_ctl_action(project_dir, sb_data, sb_p, "stop", f"standby S{idx}")

    # Stop primary
    primary_data = project_dir / "data"
    _pg_ctl_action(project_dir, primary_data, port, "stop", "primary")


def start_cluster(project_dir: Path, name: str, port: int,
                  standbys: list):
    """Start all instances in a cluster (primary first, then standbys)."""
    # Start primary
    primary_data = project_dir / "data"
    _pg_ctl_action(project_dir, primary_data, port, "start", "primary")

    # Start standbys in order
    for sb in standbys:
        idx = sb["standby_index"]
        sb_data = standby_data_dir(project_dir, idx)
        sb_p = standby_port(port, idx)
        _pg_ctl_action(project_dir, sb_data, sb_p, "start", f"standby S{idx}")


def restart_cluster(project_dir: Path, name: str, port: int,
                    standbys: list):
    """Restart all instances (stop all, then start all)."""
    stop_cluster(project_dir, name, port, standbys)
    start_cluster(project_dir, name, port, standbys)


def reload_cluster(project_dir: Path, name: str, port: int,
                   standbys: list):
    """Reload configuration for all instances."""
    primary_data = project_dir / "data"
    _pg_ctl_action(project_dir, primary_data, port, "reload", "primary")

    for sb in standbys:
        idx = sb["standby_index"]
        sb_data = standby_data_dir(project_dir, idx)
        sb_p = standby_port(port, idx)
        _pg_ctl_action(project_dir, sb_data, sb_p, "reload", f"standby S{idx}")


def build_cluster(src_dir: Path, project_dir: Path, port: int,
                  standbys: list):
    """Rebuild from source, reinstall, and restart all instances."""
    nproc = os.cpu_count() or 1

    logger.info(f"Building PostgreSQL (make -j{nproc})...")
    _run(["make", "-s", "world", f"-j{nproc}"], cwd=str(src_dir))

    logger.info("Installing PostgreSQL...")
    _run(["make", "-s", "install-world"], cwd=str(src_dir))

    restart_cluster(project_dir, project_dir.name, port, standbys)
